package infos;

public class ParkingInfo {
	private int id;
	private String parkname;
	private String parktotal;
	private String enterport;
	private String exitport;
	private String parktel;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getParkname() {
		return parkname;
	}
	public void setParkname(String parkname) {
		this.parkname = parkname;
	}
	public String getParktotal() {
		return parktotal;
	}
	public void setParktotal(String parktotal) {
		this.parktotal = parktotal;
	}
	public String getEnterport() {
		return enterport;
	}
	public void setEnterport(String enterport) {
		this.enterport = enterport;
	}
	public String getExitport() {
		return exitport;
	}
	public void setExitport(String exitport) {
		this.exitport = exitport;
	}
	public String getParktel() {
		return parktel;
	}
	public void setParktel(String parktel) {
		this.parktel = parktel;
	}
}
