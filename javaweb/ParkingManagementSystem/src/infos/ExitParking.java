package infos;

public class ExitParking {
	private int id;
	private String excardno;
	private String excarno;
	private String export;
	private String entime;
	private String extime;
	private String time;
	private String money;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getExcardno() {
		return excardno;
	}
	public void setExcardno(String excardno) {
		this.excardno = excardno;
	}
	public String getExcarno() {
		return excarno;
	}
	public void setExcarno(String excarno) {
		this.excarno = excarno;
	}
	public String getExport() {
		return export;
	}
	public void setExport(String export) {
		this.export = export;
	}
	public String getEntime() {
		return entime;
	}
	public void setEntime(String entime) {
		this.entime = entime;
	}
	public String getExtime() {
		return extime;
	}
	public void setExtime(String extime) {
		this.extime = extime;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
}
