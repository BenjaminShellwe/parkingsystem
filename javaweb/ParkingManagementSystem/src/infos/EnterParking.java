package infos;

public class EnterParking {
	private int id;
	private String encardno;
	private String encarno;
	private String enport;
	private String entime;
	private String position;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getEncardno() {
		return encardno;
	}
	public void setEncardno(String encardno) {
		this.encardno = encardno;
	}
	public String getEncarno() {
		return encarno;
	}
	public void setEncarno(String encarno) {
		this.encarno = encarno;
	}
	public String getEnport() {
		return enport;
	}
	public void setEnport(String enport) {
		this.enport = enport;
	}
	public String getEntime() {
		return entime;
	}
	public void setEntime(String entime) {
		this.entime = entime;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
}
